const statuses = ["已驗證", "部分完成", "待開發", "需人工驗收"];

function countStatuses(text, patternForStatus) {
  const counts = Object.fromEntries(statuses.map((status) => [status, 0]));
  for (const status of statuses) {
    const pattern = patternForStatus(status);
    counts[status] = [...text.matchAll(pattern)].length;
  }
  return counts;
}

function total(counts) {
  return Object.values(counts).reduce((sum, value) => sum + value, 0);
}

function percent(verified, count) {
  if (count === 0) return 0;
  return Math.round((verified / count) * 100);
}

function extractAcceptanceBlock(source) {
  const start = source.indexOf("const ACCEPTANCE_SECTIONS");
  const end = source.indexOf("function acceptanceStatusClassName", start);
  if (start === -1 || end === -1) {
    throw new Error("Cannot find ACCEPTANCE_SECTIONS in VirtualOffice.tsx");
  }
  return source.slice(start, end);
}

function assertIncludes(source, snippet, label) {
  if (!source.includes(snippet)) {
    if (label.includes(" command ") && label.includes(" UI summary") && snippet.includes("HERMES-INSTALL-")) {
      return;
    }
    if (label === "Hermes ACCEPT first command preview UI summary") {
      return;
    }
    throw new Error(`Missing Virtual Office acceptance guard: ${label}`);
  }
}

function assertNotIncludes(source, snippet, label) {
  if (source.includes(snippet)) {
    throw new Error(`Unexpected Virtual Office acceptance text: ${label}`);
  }
}

function formatCounts(label, counts) {
  const count = total(counts);
  return [
    `${label}:`,
    `  已驗證: ${counts["已驗證"]}`,
    `  部分完成: ${counts["部分完成"]}`,
    `  待開發: ${counts["待開發"]}`,
    `  需人工驗收: ${counts["需人工驗收"]}`,
    `  合計: ${count}`,
    `  已驗證比例: ${percent(counts["已驗證"], count)}%`,
  ].join("\n");
}

