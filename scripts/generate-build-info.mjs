#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = path.join(repoRoot, "server/src/build-info.generated.ts");

function readCommitSha() {
  const envSha =
    process.env.PAPERCLIP_BUILD_COMMIT_SHA ??
    process.env.GITHUB_SHA ??
    process.env.VERCEL_GIT_COMMIT_SHA ??
    process.env.COMMIT_SHA;
  if (envSha?.trim()) return envSha.trim();

  try {
    return execFileSync("git", ["rev-parse", "HEAD"], {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return "unknown";
  }
}

const buildInfo = {
  commitSha: readCommitSha(),
  builtAt: new Date().toISOString(),
};

mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  [
    "// Generated by scripts/generate-build-info.mjs. Do not edit.",
    "export const buildInfo = {",
    `  commitSha: ${JSON.stringify(buildInfo.commitSha)},`,
    `  builtAt: ${JSON.stringify(buildInfo.builtAt)},`,
    "} as const;",
    "",
  ].join("\n"),
  "utf8",
);
