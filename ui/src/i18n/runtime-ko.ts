const SKIP_TAGS = new Set([
  "CODE",
  "INPUT",
  "NOSCRIPT",
  "PRE",
  "SCRIPT",
  "STYLE",
  "TEXTAREA",
]);

let observer: MutationObserver | null = null;
let active = false;

function translateRaw(raw: string): string | null {
  const trimmed = raw.trim();
  if (!trimmed) return null;

  const exact = TEXT_TRANSLATIONS[trimmed] ?? ATTRIBUTE_TRANSLATIONS[trimmed];
  if (exact) {
    return raw.replace(trimmed, exact);
  }

  for (const [pattern, build] of TEXT_PATTERNS) {
    const match = trimmed.match(pattern);
    if (match) {
      return raw.replace(trimmed, build(match));
    }
  }

  return null;
}

function translateTextNode(node: Text) {
  const parent = node.parentElement;
  if (!parent || SKIP_TAGS.has(parent.tagName)) return;

  const next = translateRaw(node.nodeValue ?? "");
  if (next && next !== node.nodeValue) {
    node.nodeValue = next;
  }
}

function translateAttributes(element: Element) {
  for (const attr of ["aria-label", "placeholder", "title", "alt"]) {
    const value = element.getAttribute(attr);
    if (!value) continue;
    const next = translateRaw(value);
    if (next && next !== value) {
      element.setAttribute(attr, next);
    }
  }
}

function translateElement(root: ParentNode) {
  if (root instanceof Element) {
    translateAttributes(root);
  }

  const elementWalker = document.createTreeWalker(root, NodeFilter.SHOW_ELEMENT);
  while (elementWalker.nextNode()) {
    translateAttributes(elementWalker.currentNode as Element);
  }

  const textWalker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  while (textWalker.nextNode()) {
    translateTextNode(textWalker.currentNode as Text);
  }
}

export function syncKoreanRuntimeTranslation(language: string) {
  const shouldActivate = language.toLowerCase().startsWith("ko");
  active = shouldActivate;
  document.documentElement.lang = shouldActivate ? "ko" : "en";

  if (!shouldActivate) {
    observer?.disconnect();
    observer = null;
    return;
  }

  translateElement(document.body);

  if (observer) return;
  observer = new MutationObserver((mutations) => {
    if (!active) return;
    for (const mutation of mutations) {
      if (mutation.type === "characterData") {
        translateTextNode(mutation.target as Text);
        continue;
      }
      for (const node of mutation.addedNodes) {
        if (node.nodeType === Node.TEXT_NODE) {
          translateTextNode(node as Text);
        } else if (node.nodeType === Node.ELEMENT_NODE) {
          translateElement(node as Element);
        }
      }
      if (mutation.type === "attributes" && mutation.target instanceof Element) {
        translateAttributes(mutation.target);
      }
    }
  });
  observer.observe(document.body, {
    attributes: true,
    attributeFilter: ["aria-label", "placeholder", "title", "alt"],
    characterData: true,
    childList: true,
    subtree: true,
  });
}
