import type { AdapterConfigFieldsProps } from "../types";
import {
  Field,
  ToggleField,
  DraftInput,
  help,
} from "../../components/agent-config-primitives";
import { ChoosePathButton } from "../../components/PathInstructionsModal";
import { LocalWorkspaceRuntimeFields } from "../local-workspace-runtime-fields";

const inputClass =
  "w-full rounded-md border border-border px-2.5 py-1.5 bg-transparent outline-none text-sm font-mono placeholder:text-muted-foreground/40";
const instructionsFileHint =
  "Absolute path to a markdown file (e.g. AGENTS.md) that defines this agent's behavior. Injected into the system prompt at runtime. Paperclip suppresses workspace project docs for these runs so repo-scoped AGENTS.md files are not loaded twice.";

export function CodexLocalConfigFields({
  mode,
  isCreate,
  adapterType,
  values,
  set,
  config,
  eff,
  mark,
  models,
  hideInstructionsFile,
}: AdapterConfigFieldsProps) {
  const bypassEnabled =
    config.dangerouslyBypassApprovalsAndSandbox === true || config.dangerouslyBypassSandbox === true;

  return (
    <>
      {!hideInstructionsFile && (
        <Field label="Agent instructions file" hint={instructionsFileHint}>
          <div className="flex items-center gap-2">
            <DraftInput
              value={
                isCreate
                  ? values!.instructionsFilePath ?? ""
                  : eff(
                      "adapterConfig",
                      "instructionsFilePath",
                      String(config.instructionsFilePath ?? ""),
                    )
              }
              onCommit={(v) =>
                isCreate
                  ? set!({ instructionsFilePath: v })
                  : mark("adapterConfig", "instructionsFilePath", v || undefined)
              }
              immediate
              className={inputClass}
              placeholder="/absolute/path/to/AGENTS.md"
            />
            <ChoosePathButton />
          </div>
        </Field>
      )}
      <ToggleField
        label="Suppress workspace project docs"
        hint="When an instructions file is set, disable Codex workspace project-doc loading for this run so repo-scoped AGENTS.md and similar docs are not injected a second time."
        checked={
          isCreate
            ? values!.suppressWorkspaceProjectDocs ?? true
            : eff(
                "adapterConfig",
                "suppressWorkspaceProjectDocs",
                config.instructionsFilePath ? true : false,
              )
        }
        onChange={(v) =>
          isCreate
            ? set!({ suppressWorkspaceProjectDocs: v })
            : mark("adapterConfig", "suppressWorkspaceProjectDocs", v)
        }
      />
      <ToggleField
        label="Compact automation runs"
        hint="Use the automation_compact execution profile for automation-sourced wakes. This keeps search off by default, prefers lower reasoning effort, reduces prompt adornments, and uses a smaller managed CODEX_HOME unless you opt out."
        checked={
          isCreate
            ? values!.automationCompactEnabled ?? true
            : eff("adapterConfig", "automationCompactEnabled", true)
        }
        onChange={(v) =>
          isCreate
            ? set!({ automationCompactEnabled: v })
            : mark("adapterConfig", "automationCompactEnabled", v)
        }
      />
      <ToggleField
        label="Bypass sandbox"
        hint={help.dangerouslyBypassSandbox}
        checked={
          isCreate
            ? values!.dangerouslyBypassSandbox
            : eff(
                "adapterConfig",
                "dangerouslyBypassApprovalsAndSandbox",
                bypassEnabled,
              )
        }
        onChange={(v) =>
          isCreate
            ? set!({ dangerouslyBypassSandbox: v })
            : mark("adapterConfig", "dangerouslyBypassApprovalsAndSandbox", v)
        }
      />
      <ToggleField
        label="Enable search"
        hint={help.search}
        checked={
          isCreate
            ? values!.search
            : eff("adapterConfig", "search", !!config.search)
        }
        onChange={(v) =>
          isCreate
            ? set!({ search: v })
            : mark("adapterConfig", "search", v)
        }
      />
      <LocalWorkspaceRuntimeFields
        isCreate={isCreate}
        values={values}
        set={set}
        config={config}
        mark={mark}
        eff={eff}
        mode={mode}
        adapterType={adapterType}
        models={models}
      />
    </>
  );
}
