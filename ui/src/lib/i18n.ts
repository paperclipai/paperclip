export function resolveLocale(input: string | null | undefined): AppLocale {
  if (!input) return DEFAULT_LOCALE;
  const normalized = input.toLowerCase();
  if (normalized.startsWith("zh")) return "zh-CN";
  return "en";
}

export function readStoredLocale(): AppLocale | null {
  if (typeof window === "undefined") return null;
  try {
    const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    return stored ? resolveLocale(stored) : null;
  } catch {
    return null;
  }
}

export function getActiveLocale(): AppLocale {
  if (typeof document !== "undefined") {
    const docLocale = document.documentElement.lang || document.documentElement.getAttribute("data-locale");
    if (docLocale) return resolveLocale(docLocale);
  }
  const stored = readStoredLocale();
  if (stored) return stored;
  if (typeof navigator !== "undefined") {
    return resolveLocale(navigator.language);
  }
  return DEFAULT_LOCALE;
}

export function tForLocale(locale: AppLocale, key: string, params?: TranslationParams): string {
  const template = MESSAGES[locale]?.[key] ?? MESSAGES.en[key] ?? key;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => {
    const value = params[name];
    return value == null ? "" : String(value);
  });
}

export function formatNumberForLocale(value: number, locale: AppLocale = getActiveLocale()): string {
  return value.toLocaleString(locale);
}

export function formatCurrencyForLocale(
  value: number,
  locale: AppLocale = getActiveLocale(),
  currency = "USD",
): string {
  return new Intl.NumberFormat(locale, {
    style: "currency",
    currency,
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(value);
}

export function formatDateForLocale(value: Date | string, locale: AppLocale = getActiveLocale()): string {
  return new Date(value).toLocaleDateString(locale, {
    month: locale === "zh-CN" ? "numeric" : "short",
    day: "numeric",
    year: "numeric",
  });
}

export function formatDateTimeForLocale(value: Date | string, locale: AppLocale = getActiveLocale()): string {
  return new Date(value).toLocaleString(locale, {
    month: locale === "zh-CN" ? "numeric" : "short",
    day: "numeric",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit",
  });
}

export function formatShortDateForLocale(value: Date | string, locale: AppLocale = getActiveLocale()): string {
  return new Date(value).toLocaleDateString(locale, {
    month: locale === "zh-CN" ? "numeric" : "short",
    day: "numeric",
  });
}

export function relativeTimeForLocale(value: Date | string, locale: AppLocale = getActiveLocale()): string {
  const now = Date.now();
  const then = new Date(value).getTime();
  const diffSec = Math.round((then - now) / 1000);
  const absSec = Math.abs(diffSec);
  if (absSec < 60) return locale === "zh-CN" ? "刚刚" : "just now";
  const rtf = new Intl.RelativeTimeFormat(locale, { numeric: "auto", style: "short" });
  if (absSec < 3600) return rtf.format(Math.round(diffSec / 60), "minute");
  if (absSec < 86_400) return rtf.format(Math.round(diffSec / 3600), "hour");
  if (absSec < 2_592_000) return rtf.format(Math.round(diffSec / 86_400), "day");
  return formatDateForLocale(value, locale);
}
