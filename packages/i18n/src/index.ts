const englishLiteralToKey = new Map<string, MessageKey>(
  Object.entries(enMessages).map(([key, value]) => [value, key as MessageKey]),
);

function canonicalizeLocale(input: string): string {
  return input.trim().replaceAll("_", "-").toLowerCase();
}

export function matchSupportedLocale(input: string | null | undefined): SupportedLocale | null {
  if (!input) return null;
  const normalized = canonicalizeLocale(input);
  const exact = SUPPORTED_LOCALES.find((locale) => canonicalizeLocale(locale) === normalized);
  if (exact) return exact;
  const prefix = normalized.split("-")[0];
  return SUPPORTED_LOCALES.find((locale) => canonicalizeLocale(locale).split("-")[0] === prefix) ?? null;
}

export function resolveLocale(input: string | null | undefined, fallback: SupportedLocale = DEFAULT_LOCALE): SupportedLocale {
  return matchSupportedLocale(input) ?? fallback;
}

export function pickSupportedLocaleFromAcceptLanguage(header: string | null | undefined): SupportedLocale | null {
  if (!header) return null;
  const candidates = header
    .split(",")
    .map((part) => part.trim().split(";")[0]?.trim())
    .filter((value): value is string => Boolean(value));
  for (const candidate of candidates) {
    const matched = matchSupportedLocale(candidate);
    if (matched) return matched;
  }
  return null;
}

function formatMessage(template: string, params?: MessageParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_match, key) => {
    const value = params[key];
    return value === null || value === undefined ? "" : String(value);
  });
}

export function translate(locale: SupportedLocale, key: MessageKey, params?: MessageParams): string {
  const catalog = catalogs[locale] ?? catalogs[DEFAULT_LOCALE];
  return formatMessage(catalog[key] ?? catalogs[DEFAULT_LOCALE][key], params);
}

export function translateText(locale: SupportedLocale, text: string, params?: MessageParams): string {
  const key = englishLiteralToKey.get(text);
  return key ? translate(locale, key, params) : formatMessage(text, params);
}

export function translateSystemMessage(locale: SupportedLocale, message: string): string {
  return translateText(locale, message);
}

export function createTranslator(locale: SupportedLocale) {
  return {
    locale,
    t: (key: MessageKey, params?: MessageParams) => translate(locale, key, params),
    tx: (text: string, params?: MessageParams) => translateText(locale, text, params),
  };
}

export function formatDateForLocale(locale: SupportedLocale, date: Date | string): string {
  return new Intl.DateTimeFormat(locale, {
    month: "short",
    day: "numeric",
    year: "numeric",
  }).format(new Date(date));
}

export function formatDateTimeForLocale(locale: SupportedLocale, date: Date | string): string {
  return new Intl.DateTimeFormat(locale, {
    month: "short",
    day: "numeric",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit",
  }).format(new Date(date));
}

export function formatShortDateForLocale(locale: SupportedLocale, date: Date | string): string {
  return new Intl.DateTimeFormat(locale, {
    month: "short",
    day: "numeric",
  }).format(new Date(date));
}

export function formatRelativeTimeForLocale(locale: SupportedLocale, date: Date | string): string {
  const now = Date.now();
  const then = new Date(date).getTime();
  const diffSec = Math.round((now - then) / 1000);
  const rtf = new Intl.RelativeTimeFormat(locale, { numeric: "auto" });
  if (diffSec < 60) return rtf.format(-diffSec, "second");
  const diffMin = Math.round(diffSec / 60);
  if (diffMin < 60) return rtf.format(-diffMin, "minute");
  const diffHr = Math.round(diffMin / 60);
  if (diffHr < 24) return rtf.format(-diffHr, "hour");
  const diffDay = Math.round(diffHr / 24);
  if (diffDay < 30) return rtf.format(-diffDay, "day");
  return formatDateForLocale(locale, date);
}

